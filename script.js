// Take input for the first variable and multiply it by 2
var firstInput = parseFloat(prompt("Enter the first number:"));
var result = firstInput * 2;

// Take input for the second variable
var secondInput = parseFloat(prompt("Enter the second number:"));

// Add the second input to the result of the first input multiplied by 2
result += secondInput;

// Display the final result using alert() in the specified format
alert("The final result is: " + result);
